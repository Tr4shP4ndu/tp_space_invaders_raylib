// Constructor: Initializes the obstacle using its position and generates blocks based on the grid pattern
Obstacle::Obstacle(Vector2 position)
{
    this -> position = position;

    for(unsigned int row = 0; row < grid.size(); ++row) {
        for(unsigned int column = 0; column < grid[0].size(); ++column){
            if(grid[row][column] == 1) {
                float pos_x = position.x + column * 3;
                float pos_y = position.y + row * 3;
                Block block = Block({pos_x, pos_y});  // Create a block at the calculated position
                blocks.push_back(block);  // Add the block to the list of blocks
            }
        }
    }
}

// Draws each block that makes up the obstacle
void Obstacle::Draw() {
    for(auto& block: blocks) {
        block.Draw();
    }
}
